# Protocol for Zymo Quick-DNA mini-prep plus kit D4069
## Modified for winter flounder low coverage whole genome sequencing

## Digestion
### Prep (where n = number of samples):
- n + 1 labeled 2ml tubes
- n disposable razor blades and weigh boats
- n tweezers
- Preheat thermomixer to 55C


1.Add a 1cm x 1cm piece of your sample to a labeled 2 ml  tube 

2. Add 250 µl Water + 250 µl Solid Tissue Buffer (Blue) + 25 µl Proteinase K. Vortex to mix thoroughly

3. Incubate the samples at 55ºC overnight at 350rpm


## Extraction
### Prep (where n = number of samples):
- n + 1 labeled 2ml tubes
- 2(n +1) collection tubes
- n + 1 Zymo spin columns
- n  + 1 1.5ml tubes labeled with sticker
- Preheat thermomixer to 56C
- Put elution buffer on the thermomixer to warm up

### If you’re quantifying your samples immediately after the extraction, remove Biotium Qubit reagents from the 4C fridge now




1. Pellet the insoluble debris by centrifugation at 13,000rpm for 1 minute

2. Transfer all of the aqueous supernatant to a clean 2ml tube

3. IN THE PCR HOOD add 1000 µl Genomic Binding Buffer to the supernatant. Vortex to mix

4. Transfer 500 µl of the mixture to a Zymo spin column in a collection tube and centrifuge at 13,000rpm for 1 minute, then discard the flow through. Repeat as needed until all of the supernatant is passed through the column. 

5. When all the supernatant has passed through the column, discard the Collection Tube with the flow through and KEEP the spin column

6. Insert the spin column into a new Collection Tube

7. Add 400 µl DNA Pre-Wash Buffer to the spin column and centrifuge at 13,000rpm for 1 minute. Discard the flow through and keep the collection tube

8. Add 700 µl g-DNA Wash Buffer to the spin column and centrifuge at 13,000rpm for 1 minute. Discard the flow through and keep the collection tube

9. Add 200 µl g-DNA Wash Buffer and centrifuge at 13,000rpm for 1 minute. Discard the flow through AND the collection tube

10. Transfer the spin column to a clean, labeled 1.5ml microcentrifuge tube

11. Add 50 µl of warmed DNA Elution Buffer, incubate for 5 minutes uncovered, and then centrifuge at 13,000rpm for 1 minute

12. Discard the filter column. If you’re not quantifying the samples right away, store the samples in their correct box in the -20 freezer
